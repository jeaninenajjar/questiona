package QuestionC;

public class IllegalRectangle extends Exception{

	
	   private int x;
	   private int y;
	
	  
	   public IllegalRectangle(int x, int y)
	   {
	       this.x = 0;
	       this.y = 0;
	   } 
	   public int getx()
	   {
	      return x;
	   }
	   public int gety()
	   {return y;
	   }
	}
	

